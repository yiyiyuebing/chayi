package pub.makers.shop.index.entity;

import java.io.Serializable;
import java.util.Date;

public class IndentMobileModuleClassify implements Serializable{
	private static final long serialVersionUID = 1L;
	
	/**  */
	private String id;		
	
	/** 模块ID */
	private String moduleId;		
	
	/** 分类ID */
	private String classifyId;		
	
	/** 子分类ID */
	private String subClassifyId;		
	
	/** 是否热门 */
	private String isHot;		
	
	/** 排序 */
	private Integer sort;		
	
	/** 是否有效 */
	private String isValid;		
	
	/** 删除状态 */
	private String delFlag;		
	
	/** 创建时间 */
	private Date dateCreated;		
	
	/** 更新时间 */
	private Date lastUpdated;		
	

	public void setId(String id){
		this.id = id;
	}
	
	public String getId(){
		return id;
	}
	
	public void setModuleId(String moduleId){
		this.moduleId = moduleId;
	}
	
	public String getModuleId(){
		return moduleId;
	}
	
	public void setClassifyId(String classifyId){
		this.classifyId = classifyId;
	}
	
	public String getClassifyId(){
		return classifyId;
	}
	
	public void setSubClassifyId(String subClassifyId){
		this.subClassifyId = subClassifyId;
	}
	
	public String getSubClassifyId(){
		return subClassifyId;
	}
	
	public void setIsHot(String isHot){
		this.isHot = isHot;
	}
	
	public String getIsHot(){
		return isHot;
	}
	
	public void setSort(Integer sort){
		this.sort = sort;
	}
	
	public Integer getSort(){
		return sort;
	}
	
	public void setIsValid(String isValid){
		this.isValid = isValid;
	}
	
	public String getIsValid(){
		return isValid;
	}
	
	public void setDelFlag(String delFlag){
		this.delFlag = delFlag;
	}
	
	public String getDelFlag(){
		return delFlag;
	}
	
	public void setDateCreated(Date dateCreated){
		this.dateCreated = dateCreated;
	}
	
	public Date getDateCreated(){
		return dateCreated;
	}
	
	public void setLastUpdated(Date lastUpdated){
		this.lastUpdated = lastUpdated;
	}
	
	public Date getLastUpdated(){
		return lastUpdated;
	}
	
}
