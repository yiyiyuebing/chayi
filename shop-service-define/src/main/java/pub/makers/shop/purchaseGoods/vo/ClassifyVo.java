package pub.makers.shop.purchaseGoods.vo;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * Created by dy on 2017/4/20.
 */
public class ClassifyVo implements Serializable {

    private String id;
    private String name;
    private String parentId;
    private Boolean checked;
    private List<ClassifyVo> children = new ArrayList<ClassifyVo>();

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getParentId() {
        return parentId;
    }

    public void setParentId(String parentId) {
        this.parentId = parentId;
    }

    public Boolean getChecked() {
        return checked;
    }

    public void setChecked(Boolean checked) {
        this.checked = checked;
    }

    public List<ClassifyVo> getChildren() {
        return children;
    }

    public void setChildren(List<ClassifyVo> children) {
        this.children = children;
    }
}
