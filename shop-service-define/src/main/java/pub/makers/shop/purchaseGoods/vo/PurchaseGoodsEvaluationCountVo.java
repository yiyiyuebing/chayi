package pub.makers.shop.purchaseGoods.vo;

import java.io.Serializable;

/**
 * Created by kok on 2017/6/2.
 */
public class PurchaseGoodsEvaluationCountVo implements Serializable {
    private String avgScore; // 平均评分
    private Long totalCount; // 评论总数
    private Long imageCount; // 带图片评论数
    private Long goodCount; // 好评数
    private Long middleCount; // 中评数
    private Long badCount; // 差评数

    public String getAvgScore() {
        return avgScore;
    }

    public void setAvgScore(String avgScore) {
        this.avgScore = avgScore;
    }

    public Long getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(Long totalCount) {
        this.totalCount = totalCount;
    }

    public Long getImageCount() {
        return imageCount;
    }

    public void setImageCount(Long imageCount) {
        this.imageCount = imageCount;
    }

    public Long getGoodCount() {
        return goodCount;
    }

    public void setGoodCount(Long goodCount) {
        this.goodCount = goodCount;
    }

    public Long getMiddleCount() {
        return middleCount;
    }

    public void setMiddleCount(Long middleCount) {
        this.middleCount = middleCount;
    }

    public Long getBadCount() {
        return badCount;
    }

    public void setBadCount(Long badCount) {
        this.badCount = badCount;
    }
}
