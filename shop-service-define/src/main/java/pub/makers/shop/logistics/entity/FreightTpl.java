package pub.makers.shop.logistics.entity;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

public class FreightTpl implements Serializable{
	private static final long serialVersionUID = 1L;
	
	/** id */
	private String tplId;		
	
	/** 模板名称 */
	private String name;		
	
	/** 发货地址ID */
	private String addrId;		
	
	/** 发货地址名称 */
	private String addrVal;		
	
	/** 是否包邮 */
	private String freeFlag;		
	
	/** 计价方式:按件数/按重量/按体积 */
	private String priceType;		
	
	/** 指定条件包邮 */
	private String limitFreeFlag;		
	
	/** 是否有效 */
	private String isValid;		
	
	/** 删除状态 */
	private String delFlag;		
	
	/** 创建时间 */
	private Date dateCreated;		
	
	/** 更新时间 */
	private Date lastUpdated;

	private String relType;
	
	private List<FreightShipping> shippingList;
	private List<FreightPinkage> pinkageList;

	public String getRelType() {
		return relType;
	}

	public void setRelType(String relType) {
		this.relType = relType;
	}

	public void setTplId(String tplId){
		this.tplId = tplId;
	}
	
	public String getTplId(){
		return tplId;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return name;
	}
	
	public void setAddrId(String addrId){
		this.addrId = addrId;
	}
	
	public String getAddrId(){
		return addrId;
	}
	
	public void setAddrVal(String addrVal){
		this.addrVal = addrVal;
	}
	
	public String getAddrVal(){
		return addrVal;
	}
	
	public void setFreeFlag(String freeFlag){
		this.freeFlag = freeFlag;
	}
	
	public String getFreeFlag(){
		return freeFlag;
	}
	
	public void setPriceType(String priceType){
		this.priceType = priceType;
	}
	
	public String getPriceType(){
		return priceType;
	}
	
	public void setLimitFreeFlag(String limitFreeFlag){
		this.limitFreeFlag = limitFreeFlag;
	}
	
	public String getLimitFreeFlag(){
		return limitFreeFlag;
	}
	
	public void setIsValid(String isValid){
		this.isValid = isValid;
	}
	
	public String getIsValid(){
		return isValid;
	}
	
	public void setDelFlag(String delFlag){
		this.delFlag = delFlag;
	}
	
	public String getDelFlag(){
		return delFlag;
	}
	
	public void setDateCreated(Date dateCreated){
		this.dateCreated = dateCreated;
	}
	
	public Date getDateCreated(){
		return dateCreated;
	}
	
	public void setLastUpdated(Date lastUpdated){
		this.lastUpdated = lastUpdated;
	}
	
	public Date getLastUpdated(){
		return lastUpdated;
	}

	public List<FreightShipping> getShippingList() {
		return shippingList;
	}

	public void setShippingList(List<FreightShipping> shippingList) {
		this.shippingList = shippingList;
	}

	public List<FreightPinkage> getPinkageList() {
		return pinkageList;
	}

	public void setPinkageList(List<FreightPinkage> pinkageList) {
		this.pinkageList = pinkageList;
	}
	
}
