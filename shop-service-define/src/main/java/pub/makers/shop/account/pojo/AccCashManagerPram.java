package pub.makers.shop.account.pojo;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * Created by devpc on 2017/9/29.
 */
public class AccCashManagerPram implements Serializable {

    private String userName;

    private String telNumber;

    private BigDecimal cashNum;

    private String startTime;

    private String endTime;

    private String shopId;

    public String getShopId() {
        return shopId;
    }

    public void setShopId(String shopId) {
        this.shopId = shopId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getTelNumber() {
        return telNumber;
    }

    public void setTelNumber(String telNumber) {
        this.telNumber = telNumber;
    }

    public BigDecimal getCashNum() {
        return cashNum;
    }

    public void setCashNum(BigDecimal cashNum) {
        this.cashNum = cashNum;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }
}
