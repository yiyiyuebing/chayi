package pub.makers.shop.account.pojo;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * Created by dy on 2017/10/13.
 */
public class WithdrawApplyParam implements Serializable {

    private String applyId; //提现记录ID
    private String userId;  //用户ID（店铺ID）
    private BigDecimal withdrawAmount; //提现金额
    private String bankCardId; //银行卡ID


    private String reviewMan; //审核人
    private String reviewReason; //审核原因
    private String operMan; //操作人
    private String operReason; //操作原因


    public String getOperReason() {
        return operReason;
    }

    public void setOperReason(String operReason) {
        this.operReason = operReason;
    }

    public String getReviewMan() {
        return reviewMan;
    }

    public void setReviewMan(String reviewMan) {
        this.reviewMan = reviewMan;
    }

    public String getReviewReason() {
        return reviewReason;
    }

    public void setReviewReason(String reviewReason) {
        this.reviewReason = reviewReason;
    }

    public String getOperMan() {
        return operMan;
    }

    public void setOperMan(String operMan) {
        this.operMan = operMan;
    }

    public String getApplyId() {
        return applyId;
    }

    public void setApplyId(String applyId) {
        this.applyId = applyId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public BigDecimal getWithdrawAmount() {
        return withdrawAmount;
    }

    public void setWithdrawAmount(BigDecimal withdrawAmount) {
        this.withdrawAmount = withdrawAmount;
    }

    public String getBankCardId() {
        return bankCardId;
    }

    public void setBankCardId(String bankCardId) {
        this.bankCardId = bankCardId;
    }
}
