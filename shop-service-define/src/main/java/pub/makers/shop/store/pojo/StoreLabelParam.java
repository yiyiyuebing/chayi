public class StoreLabelParam implements Serializable {

    private String shopId;
    private String fansId;
    private String label;
    private String storeIds;

    private String delLabelIds;
    private String newLabelNames;

    public String getNewLabelNames() {
        return newLabelNames;
    }

    public void setNewLabelNames(String newLabelNames) {
        this.newLabelNames = newLabelNames;
    }

    public String getDelLabelIds() {
        return delLabelIds;
    }

    public void setDelLabelIds(String delLabelIds) {
        this.delLabelIds = delLabelIds;
    }

    public String getStoreIds() {
        return storeIds;
    }

    public void setStoreIds(String storeIds) {
        this.storeIds = storeIds;
    }

    public String getShopId() {
        return shopId;
    }

    public void setShopId(String shopId) {
        this.shopId = shopId;
    }

    public String getFansId() {
        return fansId;
    }

    public void setFansId(String fansId) {
        this.fansId = fansId;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
