package pub.makers.shop.store.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by dy on 2017/10/10.
 */
public class WeixinUserStoreLabel implements Serializable {
    private Long id;
    private Long weixinId;
    private Long storeId;
    private Long labelId;
    private Date dateCreated;
    private Date dateUpdate;


    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getWeixinId() {
        return weixinId;
    }

    public void setWeixinId(Long weixinId) {
        this.weixinId = weixinId;
    }

    public Long getStoreId() {
        return storeId;
    }

    public void setStoreId(Long storeId) {
        this.storeId = storeId;
    }

    public Long getLabelId() {
        return labelId;
    }

    public void setLabelId(Long labelId) {
        this.labelId = labelId;
    }

    public Date getDateCreated() {
        return dateCreated;
    }

    public void setDateCreated(Date dateCreated) {
        this.dateCreated = dateCreated;
    }

    public Date getDateUpdate() {
        return dateUpdate;
    }

    public void setDateUpdate(Date dateUpdate) {
        this.dateUpdate = dateUpdate;
    }
}
