package pub.makers.shop.promotion.vo;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;

/**
 * 商品促销信息
 * @author apple
 *
 */
public class GoodPromotionalInfoVo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -2423679880164721691L;
	
	private String goodId;
	
	private String goodSkuId;
	
	private BigDecimal price;
	
	private BigDecimal finallyAmount;
	
	private BigDecimal discountAmount;

	private PromotionActivityVo bestActivity;

	private List<PromotionActivityVo> activityList;



	public String getGoodId() {
		return goodId;
	}

	public void setGoodId(String goodId) {
		this.goodId = goodId;
	}

	public String getGoodSkuId() {
		return goodSkuId;
	}

	public void setGoodSkuId(String goodSkuId) {
		this.goodSkuId = goodSkuId;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public PromotionActivityVo getBestActivity() {
		return bestActivity;
	}

	public void setBestActivity(PromotionActivityVo bestActivity) {
		this.bestActivity = bestActivity;
	}

	public List<PromotionActivityVo> getActivityList() {
		return activityList;
	}

	public void setActivityList(List<PromotionActivityVo> activityList) {
		this.activityList = activityList;
	}


	public static long getSerialVersionUID() {
		return serialVersionUID;
	}

	public BigDecimal getFinallyAmount() {
		return finallyAmount;
	}

	public void setFinallyAmount(BigDecimal finallyAmount) {
		this.finallyAmount = finallyAmount;
	}

	public BigDecimal getDiscountAmount() {
		return discountAmount;
	}

	public void setDiscountAmount(BigDecimal discountAmount) {
		this.discountAmount = discountAmount;
	}

}
